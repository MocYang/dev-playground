# 元组Tuple
#   元组，以逗号分隔的一系列值
#   可嵌套
#   元组的元素不可变，但是元素允许是可变的类型
#   与列表不同，元组是不可变的，且元素可能包含各种类型，
#   而列表是可变的，列表的元素通常是同一种类型

t = 111, 333, 111
print(t)    # (111, 333, 111)

u = 2222, t
print(u)  # (2222, (111, 333, 111))

empty = ()
print(empty)

t1 = 'a'
print(t1, len(t1))

# 只包含一个元素的元组，通常以逗号结尾，这更高效
t2 = 'b',
print(t2, len(t2))

# 解构元组
t3 = [1, 'b', True]
x, y, z = t3
print(x, y, z)  # 1 b True








