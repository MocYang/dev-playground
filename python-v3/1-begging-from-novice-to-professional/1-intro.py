# 本章涉及的函数
# abs(number)  返回指定数的绝对值
# bytes(string, encoding[, errors]]) 对指定的字符串进行编码，并以指定的方式处理错误
# cmath.sqrt(number)    返回平方根，可用于负数
# float(object) 将字符串或数字转换为浮点数
# help([object])    提供交互式帮助
# input(prompt)     以字符串的方式，获取用户的输入
# int(object)       将字符串或数转换为整数
# math.ceil(number) 以浮点数的方式返回向上取整后的结果
# math.floor(number) 以浮点数的方式返回向下取整后的结果
# math.sqrt(number)  返回平方根，不能用于负数
# pow(x, y[, z])    返回x的y次方，对z取模的结果
# print(object, ...) 将提供的实参打印出来，用空格分隔
# repr(object)      返回指定只的字符串表示
# round(number[, ndigits)   四舍五入为指定精度，正好为5时，取偶数。
# str(obj)      将指定的值转换为字符串。用于转换bytes时，可指定编码和错误处理方式
